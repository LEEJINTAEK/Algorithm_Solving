//풀이 1
function solution(s) {
  const array = [
    "zero",
    "one",
    "two",
    "three",
    "four",
    "five",
    "six",
    "seven",
    "eight",
    "nine",
  ];

  for (let i = 0; i < array.length; i++) {
    let ex = s.slice().split(array[i]);
    s = ex.join(i);
  }
  return +s;
}

